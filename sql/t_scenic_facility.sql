-- 景区设备表
CREATE TABLE IF NOT EXISTS `t_scenic_facility` (
  `facility_id` VARCHAR(50) NOT NULL PRIMARY KEY COMMENT '设备唯一编号（建议：SCENIC_ID + 类型缩写 + 序号，如 FJ_FZ_001_WC_001）',
  `facility_name` VARCHAR(50) NOT NULL COMMENT '设备名称（如"东门游客服务中心"、"A区第三卫生间"）',
  `category` VARCHAR(20) NOT NULL COMMENT '分类：洗手间、儿童乐园、休息区、轮椅通道、互动体验、便民点、避难点、风景名胜、紧急出口',
  `icon` VARCHAR(100) DEFAULT NULL COMMENT '图标URL或图标类名（如 /static/icons/wc.svg 或 fa-toilet）',
  `position_desc` VARCHAR(100) NOT NULL COMMENT '位置描述（如"三坊七巷南后街中段东侧"）',
  `longitude` DECIMAL(10,7) DEFAULT NULL COMMENT '经度（高德/百度坐标，用于地图标注）',
  `latitude` DECIMAL(10,7) DEFAULT NULL COMMENT '纬度',
  `status` VARCHAR(50) NOT NULL COMMENT '状态：正常、维修、暂停、已拆除',
  `created_at` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  `updated_at` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '最后更新时间',
  INDEX idx_category (`category`),
  INDEX idx_status (`status`),
  INDEX idx_created_at (`created_at`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='景区设备表';

